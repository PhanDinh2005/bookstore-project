let cartTotal = 0;
const SHIPPING_FEE = 30000;

document.addEventListener("DOMContentLoaded", () => {
  // 1. Ki·ªÉm tra ƒëƒÉng nh·∫≠p
  const token = localStorage.getItem("token");
  if (!token) {
    alert("Vui l√≤ng ƒëƒÉng nh·∫≠p ƒë·ªÉ thanh to√°n!");
    window.location.href = "login.html";
    return;
  }

  // 2. T·ª± ƒëi·ªÅn t√™n user n·∫øu c√≥
  const user = JSON.parse(localStorage.getItem("user"));
  if (user && user.name) {
    document.getElementById("customer-name").value = user.name;
  }

  // 3. T·∫£i gi·ªè h√†ng
  loadCheckoutCart();
});

async function loadCheckoutCart() {
  const token = localStorage.getItem("token");
  try {
    const res = await fetch(`${API_BASE}/cart`, {
      headers: { Authorization: `Bearer ${token}` },
    });
    const result = await res.json();

    // X·ª≠ l√Ω l·∫•y m·∫£ng item
    const items = Array.isArray(result) ? result : result.data || [];

    if (items.length === 0) {
      alert("Gi·ªè h√†ng tr·ªëng! Quay l·∫°i mua s·∫Øm nh√©.");
      window.location.href = "../index.html";
      return;
    }

    renderOrderSummary(items);
  } catch (err) {
    console.error("L·ªói t·∫£i ƒë∆°n h√†ng:", err);
  }
}

function renderOrderSummary(items) {
  const container = document.getElementById("order-items-list");
  let html = "";
  cartTotal = 0;

  const formatMoney = (amount) =>
    new Intl.NumberFormat("vi-VN", {
      style: "currency",
      currency: "VND",
    }).format(amount);

  items.forEach((item) => {
    const price = item.price || 0;
    const total = price * item.quantity;
    cartTotal += total;

    html += `
            <div class="mini-item">
                <span style="font-weight:bold; color:#555; margin-right:5px;">${
                  item.quantity
                }x</span>
                <span class="mini-item-name">${item.title}</span>
                <span style="font-weight:bold;">${formatMoney(total)}</span>
            </div>
        `;
  });

  container.innerHTML = html;

  // C·∫≠p nh·∫≠t s·ªë ti·ªÅn
  document.getElementById("sub-total").innerText = formatMoney(cartTotal);
  document.getElementById("shipping-fee").innerText = formatMoney(SHIPPING_FEE);
  document.getElementById("final-total").innerText = formatMoney(
    cartTotal + SHIPPING_FEE
  );
}

// X·ª¨ L√ù KHI B·∫§M "ƒê·∫∂T H√ÄNG"
async function handleCheckout(e) {
  e.preventDefault();

  const name = document.getElementById("customer-name").value;
  const phone = document.getElementById("phone").value;
  const address = document.getElementById("address").value;
  const note = document.getElementById("note").value;

  // L·∫•y radio button ƒëang ch·ªçn
  const paymentMethod = document.querySelector(
    'input[name="payment"]:checked'
  ).value;

  const fullAddress = `${name} (${phone}) - ${address}`;

  const orderData = {
    shipping_address: fullAddress,
    customer_note: note,
    payment_method: paymentMethod,
    shipping_fee: SHIPPING_FEE,
    total_amount: cartTotal + SHIPPING_FEE,
  };

  const token = localStorage.getItem("token");
  const btn = document.querySelector(".btn-checkout");
  btn.innerText = "ƒêANG X·ª¨ L√ù...";
  btn.disabled = true;

  try {
    const res = await fetch(`${API_BASE}/orders`, {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${token}`,
      },
      body: JSON.stringify(orderData),
    });

    const data = await res.json();

    if (res.ok || data.success) {
      alert("üéâ ƒê·∫∂T H√ÄNG TH√ÄNH C√îNG! C·∫£m ∆°n b·∫°n ƒë√£ mua s√°ch.");
      // X√≥a gi·ªè h√†ng ·∫£o tr√™n giao di·ªán (Backend ƒë√£ t·ª± x√≥a r·ªìi)
      // Chuy·ªÉn v·ªÅ trang ch·ªß
      window.location.href = "../index.html";
    } else {
      alert("‚ùå L·ªói: " + data.message);
      btn.innerText = "ƒê·∫∂T H√ÄNG";
      btn.disabled = false;
    }
  } catch (err) {
    console.error(err);
    alert("L·ªói k·∫øt n·ªëi Server!");
    btn.innerText = "ƒê·∫∂T H√ÄNG";
    btn.disabled = false;
  }
}
